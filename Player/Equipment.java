package Player;
public class Equipment{ 
    int Sword , Helmet , Chestplate , Leggings , Boots;
    int Sword_Chance , Helmet_Chance , Chestplate_Chance , Leggings_Chance , Boots_Chance;
    public Equipment(){
        Sword = 0;
        Helmet = 0;
        Chestplate = 0;
        Leggings = 0;
        Boots = 0;
    }
    public void setSword(int Sword){
        this.Sword = Sword;
    }
    public int getSword(){
        return Sword;
    }
    public void setHelmet(int Helmet){
        this.Helmet = Helmet;
    }
    public int getHelmet(){
        return Helmet;
    }
    public void setChestplate(int Chestplate){
        this.Chestplate = Chestplate;
    }
    public int getChestplate(){
        return Chestplate;
    }
    public void setLeggings(int Leggings){
        this.Leggings = Leggings;
    }
    public int getLeggings(){
        return Leggings;
    }
    public void setBoots(int Boots){
        this.Boots = Boots;
    }
    public int getBoots(){
        return Boots;
    }
    public void setSword_Chance(int Sword_Chance){
        this.Sword_Chance = Sword_Chance;
    }
    public int getSword_Chance(){
        return Sword_Chance;
    }
    public void setHelmet_Chance(int Helmet_Chance){
        this.Helmet_Chance = Helmet_Chance;
    }
    public int getHelmet_Chance(){
        return Helmet_Chance;
    }
    public void setChestplate_Chance(int Chestplate_Chance){
        this.Chestplate_Chance = Chestplate_Chance;
    }
    public int getChestplate_Chance(){
        return Chestplate_Chance;
    }
    public void setLeggings_Chance(int Leggings_Chance){
        this.Leggings_Chance = Leggings_Chance;
    }
    public int getLeggings_Chance(){
        return Leggings_Chance;
    }
    public void setBoots_Chance(int Boots_Chance){
        this.Boots_Chance = Boots_Chance;
    }
    public int getBoots_Chance(){
        return Boots_Chance;
    }
}